package org.andgomes.model;

public class Player {

	private String name;
	private String position;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setPosition(String position) {
		this.position = position;
	}
	
	public String getName() {
		return name;
	}
	
	public String getPosition() {
		return position;
	}

}
