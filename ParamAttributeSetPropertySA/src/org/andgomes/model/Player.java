package org.andgomes.model;

public class Player {

	private String name;
	private String club;
	
	public void setName(String name) {
		this.name = name;
	}
	
	public void setClub(String club) {
		this.club = club;
	}
	
	public String getName() {
		return name;
	}
	
	public String getClub() {
		return club;
	}

}
